"""12. WAP to create and print the Range data type"""

print("Normal range function")
for i in range(10):
    print(i)

# we can also specify the start point of range basically it require 3 parameter start stop and step
print(" Range with start point")
for i in range(2,10):
    print(i)

print("Range with start and step")
for i in range(2,10,2):
    print(i)

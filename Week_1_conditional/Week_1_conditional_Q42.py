"""42. Notedown usages of break, continue and pass"""

"""The break statement is used to terminate the loop or statement in which it is present.
 After that, the control will pass to the statements that are present after the break statement, if available.
 If the break statement is present in the nested loop, then it terminates only those loops which contains break statement."""

"""Continue is also a loop control statement just like the break statement.
 continue statement is opposite to that of break statement, instead of terminating the loop,
  it forces to execute the next iteration of the loop.
As the name suggests the continue statement forces the loop to continue or execute the next iteration.
 When the continue statement is executed in the loop, 
 the code inside the loop following the continue statement will be skipped and the next iteration of the loop will begin."""

"""As the name suggests pass statement simply does nothing.
 The pass statement in Python is used when a statement is required syntactically but you do not want any command
  or code to execute. It is like null operation, as nothing will happen is it is executed. 
Pass statement can also be used for writing empty loops. Pass is also used for empty control statement, function and classes."""